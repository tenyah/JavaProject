//2차원 배열
public class Exam_06 {
	public static void main(String[] args) {
		int arr[][] = new int[2][3];
		
		System.out.println("arr.length : " + arr.length);//줄수
		System.out.println("arr[0].length : " + arr[0].length);//1번
		System.out.println("arr[1].length : " + arr[1].length);//2번
		
		System.out.println("arr: " + arr);//참조값
		System.out.println("arr[0]: " + arr[0]);//참조값
		System.out.println("arr[1]: " + arr[1]);//참조값
		
		System.out.println("arr[0][0]: " + arr[0][0]);//값
		
	}

}
